/**
 * Checks whether a given access level has permission to perform an action on an entity type.
 * @param {string} accessLevel - One of ACCESS_LEVELS values (e.g., 'admin', 'executive').
 * @param {string} entityType - One of ENTITY_TYPES keys (e.g., 'PORTFOLIO', 'APPLICATION').
 * @param {string} action - One of ACTIONS values (e.g., 'view', 'create', 'edit').
 * @returns {boolean} True if the action is permitted, false otherwise.
 */
export const checkPermission = (accessLevel, entityType, action) => {
  if (
    typeof accessLevel !== 'string' ||
    typeof entityType !== 'string' ||
    typeof action !== 'string'
  ) {
    return false;
  }

  const levelPerms = PERMISSION_MATRIX[accessLevel];
  if (!levelPerms) {
    return false;
  }

  const entityPerms = levelPerms[entityType];
  if (!entityPerms) {
    return false;
  }

  return entityPerms[action] === true;
};

/**
 * Returns all allowed actions for a given access level and entity type.
 * @param {string} accessLevel - One of ACCESS_LEVELS values.
 * @param {string} entityType - One of ENTITY_TYPES keys.
 * @returns {string[]} Array of allowed action strings.
 */
export const getAllowedActions = (accessLevel, entityType) => {
  if (typeof accessLevel !== 'string' || typeof entityType !== 'string') {
    return [];
  }

  const levelPerms = PERMISSION_MATRIX[accessLevel];
  if (!levelPerms) {
    return [];
  }

  const entityPerms = levelPerms[entityType];
  if (!entityPerms) {
    return [];
  }

  return ALL_ACTIONS.filter((action) => entityPerms[action] === true);
};

/**
 * Returns all entity types that a given access level can perform a specific action on.
 * @param {string} accessLevel - One of ACCESS_LEVELS values.
 * @param {string} action - One of ACTIONS values.
 * @returns {string[]} Array of entity type keys.
 */
export const getEntitiesForAction = (accessLevel, action) => {
  if (typeof accessLevel !== 'string' || typeof action !== 'string') {
    return [];
  }

  const levelPerms = PERMISSION_MATRIX[accessLevel];
  if (!levelPerms) {
    return [];
  }

  return Object.keys(levelPerms).filter(
    (entityType) => levelPerms[entityType][action] === true
  );
};

/**
 * Checks whether a given access level has any write permissions (create, edit, delete) on any entity.
 * @param {string} accessLevel - One of ACCESS_LEVELS values.
 * @returns {boolean}
 */
export const hasAnyWritePermission = (accessLevel) => {
  if (typeof accessLevel !== 'string') {
    return false;
  }

  const levelPerms = PERMISSION_MATRIX[accessLevel];
  if (!levelPerms) {
    return false;
  }

  const writeActions = [ACTIONS.CREATE, ACTIONS.EDIT, ACTIONS.DELETE];

  return Object.values(levelPerms).some((entityPerms) =>
    writeActions.some((action) => entityPerms[action] === true)
  );
};