/**
 * Helper to build a permission set from an array of allowed actions.
 * @param {string[]} allowedActions
 * @returns {Object<string, boolean>}
 */
const buildPermissions = (allowedActions) => {
  const perms = {};
  ALL_ACTIONS.forEach((action) => {
    perms[action] = allowedActions.includes(action);
  });
  return perms;
};

